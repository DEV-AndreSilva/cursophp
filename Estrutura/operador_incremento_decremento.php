<?php


//incremento (pós incremento)
echo "<br> Pós incremento <br>";
$a =10;
echo $a++;
echo "<br>";
echo $a;
echo "<br>";

//incremento (pré incremento)
echo "<br> Pré incremento <br>";
$a =10;
echo ++$a;
echo "<br>";
echo $a;
echo "<br>";

//incremento (pós decremento)
echo "<br> Pós decremento <br>";
$a =10;
echo $a--;
echo "<br>";
echo $a;
echo "<br>";

//incremento (pré decremento)
echo "<br> Pré decremento <br>";
$a =10;
echo --$a;
echo "<br>";
echo $a;

?>





